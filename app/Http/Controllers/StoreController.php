            'store' => $store,
            'queues' => Queue::select('id', 'car_id', 'status')->where('store_id', $store->id)->where('created_at', '>', now()->subDays(1))->whereIn('status', ['Waiting', 'Grooming', 'Completed'])->with('car:id,plate_no,model')->get(),
        ]);
    }

